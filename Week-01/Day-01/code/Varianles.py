#Variables

#Strings
name = "NAV"
print(name)
print(type(name))
print(id(name))

#Integers
age = 20
print(age)
print(type(age))
print(id(age))

#Floats
height = 1.75
print(height)
print(type(height))
print(id(height))

#Booleans 
#True or False
is_student = True
print(is_student)
print(type(is_student))
print(id(is_student))

#Lists
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(type(numbers))
print(id(numbers))

#Dictionaries
person = {"name": "NAV", "age": 20, "height": 1.75}
print(person)
print(type(person))
print(id(person))

#Tuples
coordinates = (1, 2, 3)
print(coordinates)
print(type(coordinates))
print(id(coordinates))

#Sets
numbers = {1, 2, 3, 4, 5}
print(numbers)
print(type(numbers))
print(id(numbers))

#None
none = None
print(none)
print(type(none))
print(id(none))